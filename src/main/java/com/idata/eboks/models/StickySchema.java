package com.idata.eboks.models;

public class StickySchema {
    private String type;
    private IndigoProperties properties;

    public String getType() {
        return type;
    }

    public void setType(String value) {
        this.type = value;
    }

    public IndigoProperties getProperties() {
        return properties;
    }

    public void setProperties(IndigoProperties value) {
        this.properties = value;
    }
}