package com.idata.eboks.models;

public class TentacledSchema {
    private String type;
    private StickyProperties properties;

    public String getType() {
        return type;
    }

    public void setType(String value) {
        this.type = value;
    }

    public StickyProperties getProperties() {
        return properties;
    }

    public void setProperties(StickyProperties value) {
        this.properties = value;
    }
}