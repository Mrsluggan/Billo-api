package com.idata.eboks.models;

public class PurpleProperties {
    private NameClass title;
    private NameClass description;
    private NameClass location;
    private NameClass infoURL;
    private NameClass startTime;
    private NameClass endTime;

    public NameClass getTitle() {
        return title;
    }

    public void setTitle(NameClass value) {
        this.title = value;
    }

    public NameClass getDescription() {
        return description;
    }

    public void setDescription(NameClass value) {
        this.description = value;
    }

    public NameClass getLocation() {
        return location;
    }

    public void setLocation(NameClass value) {
        this.location = value;
    }

    public NameClass getInfoURL() {
        return infoURL;
    }

    public void setInfoURL(NameClass value) {
        this.infoURL = value;
    }

    public NameClass getStartTime() {
        return startTime;
    }

    public void setStartTime(NameClass value) {
        this.startTime = value;
    }

    public NameClass getEndTime() {
        return endTime;
    }

    public void setEndTime(NameClass value) {
        this.endTime = value;
    }
}