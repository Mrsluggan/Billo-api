package com.idata.eboks.models;

public class XTagGroup {
    private String name;
    private String[] tags;

    public String getName() {
        return name;
    }

    public void setName(String value) {
        this.name = value;
    }

    public String[] getTags() {
        return tags;
    }

    public void setTags(String[] value) {
        this.tags = value;
    }
}